void solve() {
  int n;
  ll x;
  cin >> n >> x;

  int cnt = 0;

  for (int i = 1; i <= n; ++i) {
    if (x % i == 0) {
      ll j = x / i; 
      if (j <= n) {
        ++cnt;
      }
    }
  }

  cout << cnt << endl;
}
