void solve() {
  int a,b;cin>>a>>b;
  int tmp=0;
  if(a%4==1)tmp=a-1;
  else if(a%4==2)tmp=1;
  else if(a%4==3)tmp=a;
  else if(a%4==0)tmp=0;
  if(tmp==b){cout<<a<<endl;return;}
  if((tmp^a)==b)cout<<a+2<<endl;
  else cout<<a+1<<endl;
}
